package com.company;

public class OperatorTest{
    public static void main(String[] args) {
        int a = 3;
        System.out.println(a == 3);
        System.out.println(a != 3);
        System.out.println(a < 5);

        char a2 = 'b';
        char a1 = 'c';
        System.out.println((int)a2);
        System.out.println(0 + a2);
        System.out.println(0 + a1);
        System.out.println(a < a1);
    }
}